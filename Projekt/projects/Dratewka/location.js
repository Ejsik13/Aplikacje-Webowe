class Location {
  constructor({
    description,
    image,
    color,
    dragonParts,
    items,
    trade,
    id,
    directions,
  }) {
    this.description = description;
    this.image = image;
    this.color = color;
    this.dragonParts = dragonParts;
    this.directions = directions;
    this.items = items;
    this.trade = trade;
    this.id = id;
  }
}
